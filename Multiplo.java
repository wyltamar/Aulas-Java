package capitulo4;

public class Multiplo {

	private int numero;
	
	public Multiplo() {
		
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	@Override
	public String toString() {
		return "Multiplo [numero=" + numero + "]";
	}
	
	
}
