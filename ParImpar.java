package capitulo4;

public class ParImpar {
	
	private int numero;
	
	public ParImpar() {
		
	}
	
	public void setNumero(int num) {
		this.numero = num;
	}
	
	public int getNumero() {
		return this.numero;
	}

	@Override
	public String toString() {
		return "ParImpar [numero=" + numero + "]";
	}
	
	

}
